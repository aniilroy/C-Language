#include<stdio.h>

int main(){

    int a=12;

    int b=5;

    //Bitwise AND

    printf("a & b : %d\n", a & b);

    //Bitwise OR

    printf("a | b : %d\n", a | b);

    //Bitwise XOR 

    printf("a ^ b : %d\n", a ^ b);

    //Bitwise NOT

    printf("~a : %d\n", ~a);

    //Left shift

    printf("a << 2: %d\n", a << 2);

    //Bitwise Right shift

    printf("a >> 2 : %d\n", a >> 2);

    return 0;

}
